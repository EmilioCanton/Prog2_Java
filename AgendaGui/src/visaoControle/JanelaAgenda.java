    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nome = jFormattedTextField1.getText();
        if(nome.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor preencha"
                    + " o campo nome para que o contato seja pesquisado");
        }
        if(agenda.existeContato(nome)){
            contato = agenda.buscarContatos(nome);
            jFormattedTextField2.setText(contato.getTelefone());
            jFormattedTextField3.setText(contato.getEmail());
            JOptionPane.showMessageDialog(null, "Contato encontrado!\n"
                    + "suas informações foram escritas nos respectivos campos");    
        }
        else{
            JOptionPane.showMessageDialog(null, "Desculpe,"
                    + " o contato não foi encontrado!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        contato = new Contato(jFormattedTextField1.getText(),
                jFormattedTextField2.getText(), jFormattedTextField3.getText());
        if(contato.getNome().equals("") || contato.getTelefone().equals("") ||
                contato.getEmail().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor"
                    + " preencha todos os campos");
        }
        else if(agenda.existeContato(contato.getNome())){
            int x;
            x=JOptionPane.showConfirmDialog(null, "já existe um contato"
                    + " com esse mesmo nome, deseja sobreescreve-lo?");
            if(x==0){
                agenda.addContato(contato);
                JOptionPane.showMessageDialog(null, "Contato adicionado!");
                zeraTextos();
            }
            else{
                zeraTextos();
            }
            
        }
        else{
            agenda.addContato(contato);
            JOptionPane.showMessageDialog(null, "Contato adicionado!");
            zeraTextos();
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

